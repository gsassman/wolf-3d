#include "libft.h"

char		*ft_strcat(char *dst, const char *src)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (dst[i] != '\0')
	{
		i++;
	}
	while (src[n] != '\0')
	{
		dst[i] = src[n];
		i++;
		n++;
	}
	dst[i] = '\0';
	return (dst);
}
